'''1. Вычислить число пи c заданной точностью *d*
    *Пример:*
    - при $d = 0.001, π = 3.141.$    $10^{-1} ≤ d ≤10^{-10}$'''
    
d = float(input("Задайте точность d: "))
k = 1
pi = 0
p = int(10 / d)
for k in range(1, p):
    pi = pi + 4 * ((-1) ** (k + 1)) / (2 * k - 1)
print(f'Ответ: {str(pi)[:5]}')